"""Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями).
Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
Пользователь вводит 2 числа. n — кол-во элементов первого множества. m — кол-во элементов второго множества.
 Затем пользователь вводит сами элементы множеств."""

def common_elements(set1, set2):
    # Преобразование множеств в отсортированные списки
    list1 = sorted(list(set1))
    list2 = sorted(list(set2))
    
    # Инициализация списка для хранения общих элементов
    common = []
    
    # Индексы для обоих списков
    i = 0
    j = 0
    
    # Пока не достигнут конец хотя бы одного из списков
    while i < len(list1) and j < len(list2):
        if list1[i] == list2[j]:
            # Если элементы равны, добавляем его к общим элементам и увеличиваем оба индекса
            common.append(list1[i])
            i += 1
            j += 1
        elif list1[i] < list2[j]:
            # Если элемент из первого списка меньше, увеличиваем индекс только первого списка
            i += 1
        else:
            # Если элемент из второго списка меньше, увеличиваем индекс только второго списка
            j += 1
            
    return common

# Получаем ввод от пользователя
n = int(input("Введите количество элементов первого множества: "))
m = int(input("Введите количество элементов второго множества: "))

set1 = set()
set2 = set()

print("Введите элементы первого множества:")
for _ in range(n):
    element = int(input())
    set1.add(element)

print("Введите элементы второго множества:")
for _ in range(m):
    element = int(input())
    set2.add(element)

# Находим общие элементы и выводим их
result = common_elements(set1, set2)
print("Общие элементы в обоих множествах (в порядке возрастания):", result)
